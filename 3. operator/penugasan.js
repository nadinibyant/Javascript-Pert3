//pengurangan (-=)
let nilai1 = 10
let nilai2 = 20
nilai2 -= nilai1
console.log(nilai2)

//penjumlahan
let hasilPendapatan = 0;
let pendapatanPertama = 10;
hasilPendapatan += pendapatanPertama
console.log(`Hasil akhir pendapatan adalah : ${hasilPendapatan}`);


//perkalian
nilai2 *= nilai1
console.log(nilai2);

//pembagian (/=)
nilai2 /= nilai1
console.log(nilai2)

//sisa bagi (%=)
nilai2 %= nilai1
console.log(nilai2);
