//penjumlahan
const angka1 = 10
const angka2 = 5
const penjumlahan = angka1 + angka2
console.log(`Hasil penjumlahan adalah : ${penjumlahan}`);

//pengurangan
const pengurangan = angka1 - angka2
console.log(`Hasil pengurangan adalah : ${pengurangan}`);

//pembagian
const pembagian = angka1 / angka2
console.log(`Hasil pembagian adalah : ${pembagian}`);

//perkalian
const perkalian = angka1 * angka2
console.log(`Hasil perkalian adalah : ${perkalian}`)

//sisabagi
const sisaBagi = angka1 % angka2
console.log(`Hasil sisa bagi adalah : ${sisaBagi}`)

//pangkat
const pangkat2 = angka1 ** 2
console.log(`Hasil pangkat 2 adalah : ${pangkat2}`)
const pangkat3 = angka1 ** 3
console.log(`Hasil pangkat 3 adalah : ${pangkat3}`)

const nilaiAkar = 4
const hasilAkar = Math.sqrt(nilaiAkar)
console.log(`Hasil akar dari ${nilaiAkar} adalah : ${hasilAkar}`);