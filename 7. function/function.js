function penjumlahan (angka1, angka2) {
    const jumlah = angka1 + angka2
    return jumlah
}

function pengurangan (angka1, angka2) {
    const kurang = angka1 - angka2
    return kurang
}

const hasilPenjumlahan = penjumlahan(10, 2)
console.log(hasilPenjumlahan);

const hasilPengurangan = pengurangan(10, 2)
console.log(hasilPengurangan);